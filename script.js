/* Open the sidebar navigator*/
function openNav() {
  document.getElementById("mySidenav").style.width = "15.625rem";
}
/* close the sidebar navigator*/
function closeNav() {
  document.getElementById("mySidenav").style.width = "0rem";
}
/* Light Theme function */
function lightTheme() {
  document.body.style.backgroundColor = "lightblue";
}
/* Dark Theme function */
function darkTheme() {
  document.body.style.backgroundColor = "";
}

function cardBtn() {
  document.getElementById("cardBtn").style.borderRadius = "50%";
}
